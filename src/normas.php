				<h2><strong>Minutos esp&iacute;a</strong></h2>
				<div class="content">
	
<p><h4 >&iquest;Qu&eacute; son los minutos esp&iacute;a&#63;</h4></p>
<p>Tiempo en minutos que te permite modificar el resultado de un partido mientras se est&aacute; jugando. 
Todos los participantes al inscribirte en la porra empiezan con 10 minutos esp&iacute;a.</p>


<p><h4 >&iquest;En qu&eacute; partidos puedo utilizarlos&#63;</h4></p>
<p>Se pueden utilizar en cualquier partido, incluso en la final.</p>

<p><h4 >&iquest;Cuando puedo utilizarlos&#63;</h4></p>
<p>El tiempo empieza a contar a la hora oficial de inicio de los partidos (independientemente de los posibles retrasos).</p>

<p><h4 >&iquest;Hasta cuando puedo modificar un resultado en un partido&#63;</h4></p>
<p>Hasta 45 minutos despu&eacute;s de la hora oficial de comienzo de un partido.</p>

<p><h4 >&iquest;Cuantas veces puedo modificar el resultado de un partido&#63;</h4></p>

<p>No hay l&iacute;mite en el n&uacute;mero de modificaciones, pero cuando se cambia un resultado siempre se tiene en cuenta el tiempo que ha pasado desde la hora del inicio del partido (no desde el ultimo cambio). Ver ejemplo del final.
</p>


<p><h4 >&iquest;Cual es la unidad m&iacute;nima de tiempo que se tiene en cuenta&#63;</h4></p>
<p>El minuto. As&iacute; si se modifica el resultado a los 3 min y 46 seg de partido, se gastar&iacute;an 4 min.
</p>
<p><h4 >&iquest;C&oacute;mo se consiguen&#63;</h4></p>
<p>En cada jornada/día de la fase de grupos se podr&aacute; responder a la siguiente pregunta para obtener minutos esp&iacute;a:</p>
<p> &iquest;Cuantos goles se marcar&aacute;n entre todos los partidos de la jornada/d&iacute;a&#63;</p>
<p>Se dar&aacute;n minutos a los participantes que acierten el n&uacute;mero exacto de goles totales y los que se queden un gol arriba o abajo del n&uacute;mero exacto:</p><p><ul>
					<li>Premio de 10 minutos esp&iacute;a por acertar el n&uacute;mero exacto de goles totales en una jornada/d&iacute;a.</li>
					<li>Premio de 5 minutos esp&iacute;a por acertar uno arriba o uno abajo el n&uacute;mero de goles totales en una jornada/d&iacute;a.</li>
				</ul></p>

<p><h4 >Ejemplo de c&oacute;mo utilizar minutos esp&iacute;a</h4></p>
<p>Hora prevista del comienzo de un partido: 18:00 horas</p>
<p>
Un jugador dispone de 25 minutos esp&iacute;a, por lo tanto a partir de las 18:00 se le permitir&aacute; modificar el resultado
del partido hasta las 18:25.
Si el jugador lo modificara a las 18:07:23 gastar&iacute;a 8 minutos y le quedar&iacute;an 17 minutos esp&iacute;a.
Con estos 17 tendr&iacute;a la posibilidad de utilizarlos de nuevo en el mismo partido o dejarlos para otro partido.</p>
<p>
En el caso que quisiera utilizarlos para el mismo partido podr&iacute;a volver a modificar el resultado hasta las 18:17.
Imaginemos que lo modifica a las 18:15:45, entonces gastar&iacute;a otros 16 minutos, habiendo gastado un total de 24 minutos esp&iacute;a (8 del primer cambio + 16 del segundo cambio).
</p>
<p>De los 25 minutos esp&iacute;a iniciales que ten&iacute;a le quedar&aacute; uno, que podr&iacute;a gastar durante el primer minuto de otro partido.
</p>
